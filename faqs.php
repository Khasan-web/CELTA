					<h4 class="question-title" data-toggle="collapse" href="#faq6" role="button" aria-expanded="false" aria-controls="collapseExample">
						6. What does CELTA involve?
					</h4>
					<div class="collapse" id="faq6">
						<div class="answer my-0">
							<p>
								The CELTA course at Edu-Action is a full time course over 4 weeks with attendance at 5 days a week. The course will teach
								you the principles of effective teaching and a range of practical skills for teaching English to adult learners.
								You will have hands-on teaching practice, observe experienced teachers in the classroom and complete four practically
								focused written assignments.
							</p>
							<p>
								You will be assessed throughout the course, with no final examination. An external assessor, appointed by Cambridge ESOL,
								moderates each course. There are three components of assessment:
							</p>
							<p>
								<strong>Teaching Practice</strong> - You will teach for a total of six hours, working with classes at two levels of ability.
								Assessment is based on your overall performance at the end of the six hours.
							</p>
							<p>
								<strong>Written Assignments</strong> - you will complete four written assignments; two on teaching and learning English at
								adult level; one on the language system of English; and one on classroom teaching.
							</p>
							<p>
								<strong>Professional Development</strong> - This component assesses your overall performance on the course.
							</p>
							<p>
								Assessment on the course is continuous and trainees are given written and oral feedback by the tutors on their teaching and
								written feedback on their assignments. Trainees also have a midcourse tutorial at which they discuss their progress.
							</p>
							<p>
								All tutors working on the course discuss each trainee's final grade and their decisions are unanimous.
							</p>
							<p>
								To be awarded the Certificate, you must pass all three components. There are three grades - Pass, Pass 'B' and Pass 'A'.
								CELTA certificates don’t expire.
							</p>
